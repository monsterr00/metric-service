const сonfig = `staticcheck.json`

type сonfigData struct {
	StaticCheck []string
}

var OsExitCheckAnalyzer = &analysis.Analyzer{
	Name: "osexitcheck",
	Doc:  "check for os.Exit(1) in code",
	Run:  run,
}

